    private void asignarEncuentro(Contacto contacto) {
        if(contacto == null){
            this.limpiarCampos();
        }else{
            this.campoNombre.setText(contacto.nombre);
            this.campoNumero.setText(contacto.numero);
            this.campoRed1.setText(contacto.red1);
            this.campoRed2.setText(contacto.red2);
        }
    }

    private void limpiarCampos() {
        this.campoNombre.setText("");
        this.campoNumero.setText("");
        this.campoRed1.setText("");
        this.campoRed2.setText("");
    }

