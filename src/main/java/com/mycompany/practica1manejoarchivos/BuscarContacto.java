    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        int opcion = this.comboOpcion.getSelectedIndex();
        String campo = this.campoBusqueda.getText();
        ManejoBusqueda manejoBusqueda = new ManejoBusqueda();
        if (!(campo.isBlank() || campo.isEmpty())) {
            try {
                switch (opcion) {
                    case 0:
                        this.asignarEncuentro(manejoBusqueda.BusquedaNombre(campo, PATH));
                        break;
                    case 1:
                        this.asignarEncuentro(manejoBusqueda.BusquedaNumero(campo, PATH));
                        break;
                    case 2:
                        this.asignarEncuentro(manejoBusqueda.BusquedaRed(campo, PATH));
                        break;
                    default:
                        throw new AssertionError();
                }
            } catch (Exception e) {
                System.out.println("Error: "+e.getMessage());
            }
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

